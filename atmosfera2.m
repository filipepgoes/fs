function [ro,a] = atmosfera2(H)
 
  if H <= 11000
      ro = 1.225*(1-2.2558e-5*H).^4.256060537;
      T = 288.15-6.5e-3*H;
      a = 20.0465*sqrt(T);
  else
      ro = 0.3638879*exp(-1.576939464e-4*(H-11000));
      a = 295.065141;
  end
endfunction 